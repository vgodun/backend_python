list = [5, 12, 7, 9, 20, 15]

print('Original list: ', list)

list.append(25)
print('After adding 25: ', list)

list.remove(7)
print('After removing 7: ', list)

list.sort()
print('Sorted list: ', list)

min1=min(list)
print('Smallest number: ', min1)

max1=max(list)
print('Largest number: ', max1)
